alter session set “_ORACLE_SCRIPT”=true;

CREATE USER ADMIN_KDK
  IDENTIFIED BY ADMIN_KDK
  DEFAULT TABLESPACE USERS
  TEMPORARY TABLESPACE TEMP
  PROFILE DEFAULT
  ACCOUNT UNLOCK;
  ALTER USER ADMIN_KDK DEFAULT ROLE ALL;
  GRANT UNLIMITED TABLESPACE TO ADMIN_KDK;
  GRANT CREATE SESSION TO ADMIN_KDK;
  GRANT DBA TO ADMIN_KDK;
  ALTER USER ADMIN_KDK IDENTIFIED BY ADMIN_KDK;

alter user ADMIN_KDK identified by ADMIN_KDK;
